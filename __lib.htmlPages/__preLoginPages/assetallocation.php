              <p style="text-align: justify;">Asset allocation involves dividing an investment portfolio among different asset categories, such as stocks, bonds,mutual funds,investment partnerships,real estate, cash equivalents and private equity. It is important to decide how much money is to be invested in what and when rather than choosing in which mutual fund to invest in, this is called asset allocation. Asset allocation means different things to different people in different contexts. The concept is that the investor can lessen risk because each asset class has a different correlation to the others; for example when stocks rise, bonds often fall. The classical approach to investment management is a top-down approach that starts with Strategic Asset Allocation(SAA),in which strategic long-term decisions are made about how to allocate money across asset classes based on estimates of future returns, risks &amp; correlations. &nbsp;</span></p>
<p style="text-align: justify;">&nbsp;</span></p>
<p style="text-align: justify;">In the&nbsp;case of mutual funds, asset allocation refers to allocating money between debt and equity mutual funds. Debt and equity mutual funds have many sub-categories, equity mutual funds can be categorised as largecap, smallcap, and so on. Likewise, the sub-categories in debt mutual funds include liquid funds, ultra-short term funds&nbsp;and short-term funds. Each of these sub-categories offer different kinds of returns and are associated with varying levels of risk. When one set of funds perform poorly, the others will balance the underperformance.</span></p>
<p style="text-align: justify;">&nbsp;</span></p>
<p style="text-align: justify;">The asset allocation that works best for you at any given point in your life will depend largely on your time horizon and your ability to tolerate risk, when it comes to investing, risk and reward are inextricably entwined. You've probably heard the phrase "no pain, no gain" - those words come close to summing up the relationship between risk and reward. Before you make any investment, you should understand the risks of the investment and make sure the risks are appropriate for you. By including asset categories with investment returns that move up and down under different market conditions within a portfolio, an investor can protect against significant losses. Historically, the returns of the three major asset categories have not moved up and down at the same time.</span></p>
<p style="text-align: justify;">&nbsp;</span></p>
<p style="text-align: justify;">Market conditions that cause one asset category to do well often cause another asset category to have average or poor returns. By investing in more than one asset category can reduce the risk that you'll lose money and your portfolio's overall investment returns will have a smoother ride. If one asset category's investment return falls, you'll be in a position to counteract your losses in that asset category with better investment returns in another asset category. "Don't put all your eggs in one basket." The strategy involves spreading your money among various investments in the hope that if one investment loses money, the other investments will more than make up for those losses.</span></p>
<p style="text-align: justify;">&nbsp;</span></p>
<p style="text-align: justify;">In addition, asset allocation is important because it has major impact on whether you will meet your financial goal. If you don't include enough risk in your portfolio, your investments may not earn a large enough return to meet your goal. A portfolio heavily weighted in stock or stock mutual funds, for instance, would be inappropriate for a short-term goal, such as saving for a family's summer vacation.</span></p>
<p style="text-align: justify;">&nbsp;</span></p>
<p style="text-align: justify;"><strong>There is no single asset allocation model that is right for every financial goal and</strong> <strong>no person&rsquo;s financial condition remains the same.&nbsp;This means that one would have to change their investment strategy from time to time.</strong></span></p>
