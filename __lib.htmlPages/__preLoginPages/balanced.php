<strong>Which is best?</strong>

<p>One can get a doubt that which is best to invest either systematic investment plan (SIP) or Lumpsum investment. There is a difference between the cash flows – in case of lump sum investing, the investor has money in hand that can be invested. Whereas in case of SIP, the investor may not have lump sum in hand and may have regular surplus expected in future. If a person does not have lump sum money available to invest, there is no question of one investing at one go. Similarly, if there is a person whose future income is uncertain, SIP is out of question.</p>
<p class="title-p" style="text-align: center;"> <img src="<?php echo $CONFIG->staticURL;?><?php echo $CONFIG->theme; ?>img/balanced2.png"  ></p>

It is different from person to person<br><br>

<strong>Salaried investor:</strong> A salaried person has a regular cash flow; hence, investing in SIP is a better approach. However, if you get a lump sum in the form of bonus etc. you may look to invest it in a lump sum though with some precautions.<br><br>

<strong>New Investors:</strong> If you are investing for the first time, do take the SIP route. The markets are volatile and seeing the value of the investment going down can cause jitters for any investor and more so for a new investor who is not used to market volatilities.<br><br>

<strong>Investing for Short Term:</strong> If you are investing for short term, say for 3 years to buy a car, then you should go for lump sum and get your money to work immediately. Also, since for short term goals debt funds are a better choice, lump sum will give you better returns than SIP.<br><br>


<strong>Windfall gain:</strong> In case you get money as a windfall, for example a bonus, then you may use the STP (Systematic Transfer Plan) option look to invest it as a lump sum in a liquid fund and then transfer a fixed amount per month to an Equity Fund of your choice. 
Here are few of the main conclusions of SIP or lump sum which you can derive from this post.<br><br>

<ul>
  <li style="list-style-type: disc;">SIP can reduce the market fluctuation risks by Rupee cost averaging.</li>
<li style="list-style-type: disc;">Invest in the lump sum when the market is continuously rising.</li>
<li style="list-style-type: disc;">Invest in SIP when the prices are falling/</li>

</ul>

