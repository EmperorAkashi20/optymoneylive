<p>All these questions are answered with the Will, who is otherwise called as <strong>Testament</strong>. Will is a legal declaration by which a person, the testator, names one or more persons to manage his/her estate and provides for the transfer of his/her property at the time of death. It can be made by anyone above 21 years of age in India. It is a statement made by a testator in the written form stating the manner in which his property must be distributed after his death.You are able to appoint a guardian for the care of your minor children. Your children are probably the most important part of your life. If you have children, it’s important to make sure that they’re always protected and cared for. By appointing a guardian in your will, you will be able to do this.</p>

<p>Many people do not write a Will and those who die without writing a Will is called an <strong>intestate</strong>. Dying an intestate leaves the loved ones in dispute. There are many court cases which are being fought for distribution of petty amount like Rs 5000 in the Indian court since the distribution is a complex process. Life is unpredictable and therefore it is advised to write a Will no matter how young or how poor you seem, one less dispute means a lot of time saved in the court.
</p>

<p style="text-align: justify;"><em><span style="font-size: 12.0pt; line-height: 115%; color: #e36c0a;">Lets&rsquo;s go into the story which we all know.</span></em></p>

<p>The classic case of Reliance industry is one such example. Assuming the two brothers will stay united and lead the industries of Late Dhirubhai Ambani who did not write a Will and died Intestate. He left behind property worth Rs 990 billion, there was a dispute among the family including two brothers and their mother Kokilaben. Reliance Energy, Reliance Infocommand Reliance capital were decided to be headed by younger brother Anil Ambani and the heart Reliance Oil and gas were given to the elder brother Mukesh Ambani. The property was dived in the ratio 30:30:40 among Anil Ambani, Mukesh Ambani and Kokilaben. Ten per cent of the mother's share could, in turn, be split between the two Ambani daughters. 
After her death, her 30 per cent share would be divided between Anil Ambani and Mukesh Ambani. We all know Reliance would have been much larger if both the brothers worked together. 
</p>


<p>There are two kinds of Will, Privileged Wills and Unprivileged Wills. Privileged will is one which is made by any soldier, airman, navy persons, a mariner who are willing to dispose of their estate during their course of employment. A soldier includes officers and all other rank officers of service but does not include a civilian engineer employed by the army, having no military status. The un privelaged will is one which is created by every testator not being asoldier, airman, mariner.</p>


<p style="text-align: justify;"> <em><span style="font-size: 12.0pt; line-height: 115%; color: #e36c0a; background: white;">What</span></em> <em><span style="font-size: 12.0pt; line-height: 115%; color: #e36c0a; background: white;"> if my dad decides to write another Will? </span></em></strong></p>
<p style="text-align: justify;"> <em><span style="font-size: 12.0pt; line-height: 115%; color: #e36c0a; background: white;">Or decides to add another piece of property to be divided after his death?</span></em></strong></p>

<p>Well in that case the Will can be revoked by the testator only by another Will or by some writing declaring an intention to revoke the same and to be executed in the manner in which an unprivileged Will can be executed under the Act or by burning, tearing or destroying of the same by the testator or by some other person in his presence and by his directions with the intention of revoking the same.</p>
 <p style="text-align: justify;"><strong><em><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; color: #76923c;">How easy to create a Will of your choice?</span></em></strong></p>
  
 

<p>Did you know you can write a Will on a plain sheet of paper? </p>
It may be shocking, but yes, you can write a Will on your own without seeking the help of lawyers and other professional agencies which charge a hefty consultation fee. There are only two things to be taken care of while writing the Will because only on the basis of these two criteria the will can be challenged in the court of law. The first and foremost is to mention your credentials according to the identification proofs issued by the government.</p>
 <p>For example, writing your name as it is written in the Aadhar card, Passport and Birth certificate etc. without any grammatical mistakes because there are many instances where the Will has been declared void because the name the Voter ID was “Sanjay R Gupta” but the name mentioned in the Will was “Sanjay Gupta”.Due to this single mistake, the Gupta family has to suffer a huge loss. Also if the Testator is above the age of 50, one must attach a health certificate from a government hospital stating he or she is mentally and physically fit to execute the Will. The second thing is the Testator must not be under any influence, coercion or force during the execution of the Will. These two things must be written under the self-declaration and signed by two witnesses who are not-beneficiaries of the Will. A Will can be made by the deaf and dumb person by showing consent through writing or gestures in sign language. Nothing prevents a person in India from drawing a Will. You also need to mention your relationship with beneficiary while writing the Will. Usually, the list of assets and beneficiaries are created separately but it is strongly advised against doing so. In order to avoid any misunderstandings, you must write a separate paragraph for each beneficiary depicting the list of assets assigned to him or her. The next thing you need is an executor, who must not be a beneficiary of the Will. He or she must be a neutral person and need not be a lawyer.You will have a greater peace of mind. Knowing that you have a proper plan in place will allow you and your family to feel more at ease. Without a will, you may have fears about how future affairs will be handled.</p>



 

