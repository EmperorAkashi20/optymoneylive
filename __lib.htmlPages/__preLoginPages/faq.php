                              <button class="btn btn-link" type="button" data-toggle="collapse" data-target="#accordion-tab-1-content-3" aria-expanded="false" aria-controls="accordion-tab-1-content-3">Distribution of surplus cash funds from companies (Private limited company)</button>
                        </div>
                        <div class="collapse" id="accordion-tab-1-content-3" aria-labelledby="accordion-tab-1-heading-3" data-parent="#accordion-tab-1">
                           <div class="card-body">
                              The probable ways of withdrawal of funds / distribution of funds of a private limited company under different case and its Tax implication is discussed as hereunder:
                              <strong><em>Case 1: In case of voluntary winding up of company</em></strong>
                              In case a company has voluntarily passed the resolution for winding up / liquidation and the company has no other debts/ loan / creditors outstanding at the time of liquidation, in such case the company can distribute the funds available with the company to the shareholders which could be in the nature of <strong>Income or Capital.</strong>
                              &nbsp;
                              <ul>
                                 <li><strong><u>Income-</u></strong>if a distribution to members is from income generated by the company through trading activities or operation activities, then it will be <strong>deemed to be a dividend paid</strong> to members out of profits derived from the company i.e. distribution to the extent of accumulated Reserve &amp; Surplus (Retained Earnings) of the company would be termed as Deemed Dividend u/s 2(22)(d) in the hands of the Shareholders. On this dividend the company would be required to pay <strong>Dividend Distribution Tax (DDT) </strong>of 15% + surcharge + Cess (16.995% effective Rate).</li>
                                 <li><strong><u>Capital -</u></strong> Because the liquidation of a company gives rise to the cancellation of the shares in the company, a capital gain or loss could arise. In simple terms, a capital gain would arise for the shareholders if the capital proceeds from the cancellation of the shares exceed the cost base of the shares. The time the capital gain arises is when the shares are cancelled (i.e. at the time the company is dissolved). As such Capital Gain Tax (CGT) has to be calculated <strong>for the members</strong> in accordance to <strong>section 46(2) of Income Tax Act</strong>.
                                    <strong><em>Case 2: In case of buy back of shares of company</em></strong> An obvious question that comes to one’s mind is whether buyback, as a means of surplus cash distribution, is more tax efficient over dividend pay-outs. The answer is not straightforward since it depends on several variable factors such as nature of shares (listed/unlisted), residential status of the investor, period of holding, etc.
                                 </li>
                                 <li><strong>Buyback of shares listed on recognised stock exchange in India</strong>
                                    There are generally no tax implications in the hands of company on buyback of its shares under the modes prescribed under Securities and Exchange Board of India (Buy back of Securities) Regulations, 1998, since Dividend Distribution Tax (DDT) and buyback tax (BBT) are not triggered.
                                    The buyback of shares listed on a stock exchange can be considered as a tax-efficient mode of surplus cash distribution from the company’s standpoint.
                                    <strong>In the hands of the shareholders</strong> (irrespective of residential status), a buyback triggers capital gain tax since exemption under Section 10(34A) is available only in cases where BBT is paid by the company. The buyback of listed shares held for over a year, qualifies as long term capital gain (LTCG) and the same is tax exempt under Section 10(38) of the Act if shares are bought back before March 31, 2018.
                                    Else, the same may trigger capital gain tax implications in the hands of shareholders. However, MAT implications would get trigged in the hands of resident corporate shareholders.<br>
                                    In case the shares are held for a period less than a year, the gains would qualify as short term capital gain (STCG) and would generally be taxable at an effective tax rate of 16.22% (inclusive of surcharge &amp; cess). <br>
                                    It will be worthwhile to note that a non-resident investor possessing a valid Tax Residency Certificate can avail the beneficial provisions of the relevant Double Tax Avoidance Agreement entered into by the Indian government with its tax residence country.<br>
                                    In case the shares are held as stock-in trade, then any gain arising out of buyback would be taxable as business income as per commercial principles.
                                    <br>However, if gains are arising to non-resident shareholders pursuant to buyback, the company is required to withhold tax at the applicable rates. Absence of adequate details of non-resident investors in case of buybacks executed over a stock exchange, could impose certain practical challenges for the company for discharging its withholding tax obligations.
                                 </li>
                                 <li><strong>Buyback of unlisted shares</strong>
                                    A domestic company is required to pay BBT at the rate of 23.072% (inclusive of surcharge and cess) on buyback of its unlisted shares implemented under any mode prescribed under the Companies Act, 1956. BBT is an additional tax in the hands of the domestic company over and above its income tax liability. <br>
                                    In terms of Section 10(34A), a shareholder participating in such a buyback scheme enjoys tax exemption, irrespective of whether shares are held as capital assets or stock-in-trade and irrespective of availability of treaty benefit. Further, no MAT liability is triggered in the hands of a corporate shareholder.<br>
                                    These provisions, compared with the provisions of taxability of dividend distribution, make the buyback of shares a tax efficient manner of distribution of surplus cash, especially in case of large shareholders subject to tax on dividend receipt (Individuals, HUFs, certain trusts, etc).
                                 </li>
                              </ul>
