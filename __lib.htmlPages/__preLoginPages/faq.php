                        <div class="collapse" id="accordion-tab-6-content-4" aria-labelledby="accordion-tab-6-heading-4" data-parent="#accordion-tab-6">
                           <div class="card-body">
                              <p>Any profit or gain arising from transfer of a capital asset [as defined u/s 2(14) of IT Act,1961] during the year is charged to tax under the head “Capital Gains”.</p>
                           </div>
                        </div>
                     </div>
                     <div class="card">
                        <div class="card-header" id="accordion-tab-6-heading-5">
                          
                              <button class="btn btn-link" type="button" data-toggle="collapse" data-target="#accordion-tab-6-content-5" aria-expanded="false" aria-controls="accordion-tab-6-content-5">What is the meaning of capital asset?</button>
                          
                        </div>
                        <div class="collapse" id="accordion-tab-6-content-5" aria-labelledby="accordion-tab-6-heading-5" data-parent="#accordion-tab-6">
                           <div class="card-body">
                              <p>Capital asset is defined to include:
                              <li>a) Any kind of property held by an assesse, whether or not connected with business or profession of the assesse.<br>
                              <li>b) Any securities held by a FII which has invested in such securities in accordance with the regulations made under the SEBI Act, 1992.<br>
                              </ol>
                              However, the following items are&nbsp;<strong><em>excluded</em></strong>&nbsp;from the definition of "capital asset":
                              <ul>
                                 <li>Any stock-in-trade, consumable stores, or raw materials held by a person for the purpose of his business or profession.<br>
                              </ul>
                              E.g., Motor car for a motor car dealer or gold for a jewellery merchant, are their stock-in-trade and, hence, they are not capital assets for them.
                              <ul>
                                 <li>Personal effects of a person, that is to say, movable property including wearing apparels (*) and furniture held for use, by a person or for use by any member of his family dependent on him.<br>
                              </ul>
                              (*) However, jewellery, archeological collections, drawings, paintings, sculptures, or any work of art are not treated as personal effects and, hence, are included in the definition of capital assets.
                              The term jewellery has been given a wider meaning and includes ornaments made up of gold, silver, platinum or any other precious metal or any alloy containing one or more of such precious metals, whether or not containing any precious or semi-precious stones, and whether or not worked or sewn into any wearing apparel. It also includes precious or semi-precious stones, whether or not set in any furniture, utensil, or other article or worked or sewn into any wearing apparel.
                              <ul>
                                 <li>Agricultural Land in India, not being a land situated:<br>
                              </ul>
                              <li>Within jurisdiction of municipality, notified area committee, town area committee, cantonment board and which has a population of not less than 10,000;<br>
                              <li>Within range of following distance measured aerially from the local limits of any municipality or cantonment board:<br>
                              </ol>
                              <ul>
                                 <li>not being more than 2 KMs, if population of such area is more than 10,000 but not exceeding 1 lakh;<br>
                              </ul>
                              <li>not being more than 6 KMs , if population of such area is more than 1 lakh but not exceeding 10 lakhs; or<br>
                              <li>not being more than 8 KMs , if population of such area is more than 10 lakhs.<br>
                              </ol>
                              Population is to be considered according to the figures of last preceding census of which relevant figures have been published before the first day of the year.
                              <ul>
                                 <li>6&frac12;% Gold Bonds,1977 or 7% Gold Bonds, 1980 or National Defense Gold Bonds, 1980 issued by the Central Government.<br>
                                 <li>Special Bearer Bonds, 1991, issued by the Central Government<br>
                                 <li>Gold Deposit Bonds issued under Gold Deposit Scheme, 1999.<br>
                                 <li>Deposit certificates issued under the Gold Monetization Scheme, 2015.<br>
                              </ul>
                              <em><u>Following points should be kept in mind :</u></em>
                              The property being capital asset may or may not be connected with the business or profession of the taxpayer. E.g. Bus used to carry passenger by a person engaged in the business of passenger transport will be his Capital asset.
                              Any securities held by a Foreign Institutional Investor which has invested in such securities in accordance with the regulations made under the Securities and Exchange Board of India Act, 1992 will always be treated as capital asset, hence, such securities cannot be treated as stock-in-trade.</p>
                           </div>
