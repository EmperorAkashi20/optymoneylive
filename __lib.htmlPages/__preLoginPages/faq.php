                              <button class="btn btn-link" type="button" data-toggle="collapse" data-target="#accordion-tab-11-content-2" aria-expanded="false" aria-controls="accordion-tab-11-content-2">After filing Return of Income u/s 139 for a Financial year, What notice that can be received in its respect?</button>
                           </h5>
                        </div>
                        <div class="collapse" id="accordion-tab-11-content-2" aria-labelledby="accordion-tab-11-heading-2" data-parent="#accordion-tab-11">
                           <div class="card-body">
                              <p>After successful filing of Return of Income by an assessee u/s 139, the major notices that can be expected to come within a period of 1 year from the end of Financial Year in which the said return was filed is&nbsp;<u>Intimation Notice U/s 143(1)</u>,&nbsp;<u>Notice for Scrutiny Assessment u/s 143(2) ( within a period of 6 months from the end of the Financial Year in which the said return was filed)&nbsp;</u>and&nbsp;<u>Notice for Best Judgement Assessment u/s 142(1).</u><br>
In addition to above, a&nbsp;<u>notice u/s 133C</u>&nbsp;as inserted by the Finance (No.2) Act 2014 w.e.f 01/10/2014 can also be issued by the prescribed Income Tax authority for Return of Income filed for AY 2015-16 and onwards within a time period of 4 years or 6 years as prescribed u/s 153.<br>
Every taxpayer has to furnish the details of his income to the Income-tax Department. These details are to be furnished by filing up his return of income. Once the return of income is filed up by the taxpayer, the next step is the processing of the return of income by the Income Tax Department. The Income Tax Department examines the return of income for its correctness. The process of examining the return of income by the Income Tax department is called as &ldquo;<strong>Assessment</strong>&rdquo;. Assessment also includes&nbsp;<strong>re-assessment</strong>&nbsp;and&nbsp;<strong>best judgment assessment&nbsp;</strong>under section 144.<br>
Under the Income-tax Law, there are four major assessments given below:<br>
 
<li>Assessment under section 143(1), i.e., Summary assessment without calling the assessee.</li>
