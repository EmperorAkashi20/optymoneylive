                              <button class="btn btn-link" type="button" data-toggle="collapse" data-target="#accordion-tab-6-content-12" aria-expanded="false" aria-controls="accordion-tab-6-content-12">What are the provisions relating to computation of capital gain in case of transfer of asset by way of gift, will, etc.?</button>
                           
                        </div>
                        <div class="collapse" id="accordion-tab-6-content-12" aria-labelledby="accordion-tab-6-heading-12" data-parent="#accordion-tab-6">
                           <div class="card-body">
                              <p>Capital gain arises if a person transfers a capital asset. section 47 excludes various transactions from the definition of 'transfer'. Thus, transactions covered under section 47 are not deemed as 'transfer' and, hence, these transactions will not give rise to any capital gain.  Transfer of capital asset by way of gift, will, etc., are few major transactions covered in section 47. Thus, if a person gifts his capital asset to any other person, then no capital gain will arise in the hands of the person making the gift (*).<br><br>
                                 If the person receiving the capital asset by way of gift, will, etc. subsequently transfers such asset, capital gain will arise in his hands. Special provisions are designed to compute capital gains in the hands of the person receiving the asset by way of gift, will, etc. In such a case, the cost of acquisition of the capital asset will be the cost of acquisition to the previous owner and the period of holding of the capital asset will be computed from the date of acquisition of the capital asset by the previous owner.<br><br>
                                 (*) As regards the taxability of gift in the hands of person receiving the gift, separate provisions are designed under section 56.
