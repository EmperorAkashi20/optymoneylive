                     <div class="card">
                        <div class="card-header" id="accordion-tab-6-heading-6">
                       
                              <button class="btn btn-link" type="button" data-toggle="collapse" data-target="#accordion-tab-6-content-6" aria-expanded="false" aria-controls="accordion-tab-6-content-6">What is the meaning of the term ‘long-term capital asset’?</button>
                          
                        </div>
                        <div class="collapse" id="accordion-tab-6-content-6" aria-labelledby="accordion-tab-6-heading-6" data-parent="#accordion-tab-6">
                           <div class="card-body">
                              <p>Any capital asset held by a person for a period of more than 36 months immediately preceding the date of its transfer will be treated as long-term capital asset.<br>
                                 <strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</strong>Any capital asset held by a person for a period of not more than 36 months immediately preceding the date of its transfer will be a short-term capital asset.<br>
                                 However, in respect of certain assets like shares (equity or preference) which are listed in a recognised stock exchange in India, units of equity oriented mutual funds, listed securities like debentures and Government securities, Units of UTI and Zero Coupon Bonds, the period of holding to be considered is 12 months instead of 36 months.<br>
                                 In case of unlisted shares in a company, the period of holding to be considered is 24 months instead of 36 months.<br>
                                 <strong><em>Illustration</em></strong><br>
                                 Mr. Raj is a salaried employee. On 8th April, 2014, he purchased a piece of land and sold the same on 29th June, 2016. In this case, land is a capital asset for Mr. Raj. He purchased the land on 8th April, 2014 and sold it on 29th June, 2016, i.e., after holding it for a period of less than 36 months. Hence, land will be a short-term capital asset.<br>
                                 <strong><em>Illustration</em></strong><br>
                                 Mr. Kumar is a salaried employee. On 8th July, 2015, he purchased shares of SBI Ltd. (listed in BSE) and sold the same on 29th June, 2016. In this case, shares are capital assets for Mr. Kumar. He purchased shares on 8th July, 2015 and sold them on 29th June, 2016 i.e., after holding them for a period of less than 12 months. Hence, shares are short-term capital assets.
                              </p>
                           </div>
                        </div>
