                                    <strong><em>Illustration</em></strong><br>
                                    Mr. Raja sold his bungalow for Rs. 80,00,000. The value adopted by the Stamp Valuation Authority of the bungalow for the purpose of payment of stamp duty is Rs. 84,00,000. In this situation, while computing taxable capital gain arising on transfer of bungalow, Rs. 84,00,000 will be taken as full value of consideration (i.e., sale value of the bungalow). Thus, actual selling price of Rs. 80,00,000 (being less than stamp duty value) will not be taken into account while computing taxable capital gain.<br>
