                        <div class="card-header" id="accordion-tab-6-heading-7">
                          
                              <button class="btn btn-link" type="button" data-toggle="collapse" data-target="#accordion-tab-6-content-7" aria-expanded="false" aria-controls="accordion-tab-6-content-7">What is long-term capital gain and short-term capital gain?</button>
                          
                        </div>
                        <div class="collapse" id="accordion-tab-6-content-7" aria-labelledby="accordion-tab-6-heading-7" data-parent="#accordion-tab-6">
                           <div class="card-body">
                              <p>Gain arising on transfer of long-term capital asset is termed as long-term capital gain and gain arising on transfer of short-term capital asset is termed as short-term capital gain. However, there are a few exceptions to this rule, like gain on depreciable asset is always taxed as short-term capital gain.</p>
