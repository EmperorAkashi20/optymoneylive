                        <div class="card-header" id="accordion-tab-6-heading-8">
                          
                              <button class="btn btn-link" type="button" data-toggle="collapse" data-target="#accordion-tab-6-content-8" aria-expanded="false" aria-controls="accordion-tab-6-content-8">Why capital gains are classified as short-term and long-term?</button>
                          
                        </div>
                        <div class="collapse" id="accordion-tab-6-content-8" aria-labelledby="accordion-tab-6-heading-8" data-parent="#accordion-tab-6">
                           <div class="card-body">
                              <p>The taxability of capital gain depends on the nature of gain, i.e. whether short-term or long-term. Hence to determine the taxability, capital gains are classified into short-term capital gain and long-term capital gain. In other words, the tax rates for long-term capital gain and short-term capital gain are different. Similarly, computation provisions are different for long-term capital gains and short-term capital gains.</p>
