<li>Loss from non speculative business can be carried forward for eight years immediately next to the year in which the loss is incurred.</li>
 
<strong>Loss from owning and maintaining race horses:</strong><br>
 
<li>Loss from the business of owning and maintaining race horses cannot be set off against any income other than income from the business of owning and maintaining race horses. Such loss can be carried forward only for a period of 4 years</li>
 
<strong>Speculative Business loss:</strong><br>
 
<li>If loss of any speculative business cannot be fully adjusted in the year in which it is incurred, then the unadjusted speculative loss can be carried forward for making adjustment in the next year. In the subsequent years such loss can be adjusted only against income from speculative business (may be same or any other speculative business). Such loss can be carried forward for 4 years immediately succeeding the year in which the loss is incurred.</li>
 
<strong>Specified Business loss:</strong><br>
 
<li>Loss from business specified under section 35AD cannot be set off against any other income except income from specified business. Such loss can be carried forward for adjustment against income from specified business for any number of years</li>
<li>Section 35AD is applicable in respect of certain specified businesses like setting up a cold chain facility, setting up and operating warehousing facility for storage of agricultural produce, developing and building a housing projects, etc.</li>
 
