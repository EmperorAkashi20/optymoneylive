Many times it may happen that after making intra-head and inter-head adjustments, still the loss remains unadjusted due to inadequacy of eligible profits, such losses are carried forward to the next assessment year for adjustment against the eligible profits of that year. The maximum period for which different losses can be carried forward for set-off has been provided in the Income Tax Act.<br>
<strong>Carry forward and set off of business loss:</strong><br>
 
<li>If loss of any business/profession cannot be fully adjusted in the year in which it is actually incurred, then the unadjusted loss can be carried forward to the next year for making adjustment. In the next years such loss can be adjusted only with the income under the head &ldquo;Profits and gains of business or profession&rdquo;</li>
