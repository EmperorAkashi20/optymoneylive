                        <div class="collapse show" id="accordion-tab-1-content-1" aria-labelledby="accordion-tab-1-heading-1" data-parent="#accordion-tab-1">
                           <div class="card-body">
                              <p>As per the provisions of Section 139, every assessee whose gross total income does not exceeds the basic exemption limit for any financial year, he will be relieved from filing the return for the said assessment year.</p>
                              <p>However, as per the amended provisions of section 139(1), where the gross total income of the assessee after including the exempted income u/s 10(38) i.e. Long term capital gain on sale of listed securities, exceeds the basic exemption limit, he shall be compulsorily required to file the Income Tax Return for the said financial year even if his net taxable income is less than the basic exemption limit.</p>
                              <p>This means that even if your taxable income is less than the basic exemption limit of Rs. 2,50,000 but because of exempt LTCG income, the total amount exceeds Rs 2,50,000, then filing of return of income is mandatory. The above provisions are applicable with effect from 1.04.2017.</p>
