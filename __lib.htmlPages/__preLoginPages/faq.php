                        <div class="collapse show" id="accordion-tab-3-content-1" aria-labelledby="accordion-tab-3-heading-1" data-parent="#accordion-tab-3">
                           <div class="card-body">
                              Section 197 facilitates a tax payer whose TDS is being deducted to apply for a Lower Tax Deduction Certificate by filing <strong>Form 13 </strong>to the Income Tax Officer in case his tax liability is less than the amount of TDS that is being deducted.
                              <p>Once such a certificate is granted by the officer on him being satisfied that lower deduction of TDS is justified, the TDS will be deducted as per the TDS rate stated therein.
                                 This certificate is required to be submitted to the person who is deducting the TDS in all the cases except where payment is being made as Interest on securities or Interest on fixed deposits U/S 197A where Form 15 G/ Form 15 H has to be submitted.
                                 <em><u>Procedure for filing Form 13 to the Income Tax Officer:</u></em> 
                              </p>
                              Various details that are required to be furnished by the tax payer while making an application in Form 13 are as below:
                              <ul>
                                 <li>Name and PAN no.</li>
                                 <li>Details regarding the purpose for which the payment is being received</li>
                                 <li>Details of Income for the last 3 years and the projected current year’s income.</li>
                                 <li>Details of payment of tax of the last 3 years.</li>
                                 <li>Details of tax deducted / paid for the current year.</li>
                              </ul>
                              <p>As per section 28AA, the certificate so issued by the income tax officer under section 197 is valid only for the assessment year mentioned in the certificate unless cancelled before the expiry of the date mentioned in the certificate.</p>
