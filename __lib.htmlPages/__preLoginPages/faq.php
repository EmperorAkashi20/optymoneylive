                           </h5>
                        </div>
                        <div class="collapse show" id="accordion-tab-10-content-1" aria-labelledby="accordion-tab-10-heading-1" data-parent="#accordion-tab-10">
                           <div class="card-body">
                              <p><strong>Concept of set-off:</strong><br>
Specific provisions have been made in the Income-tax Act, 1961 for the set-off and carry forward of losses. In simple words, &ldquo;Set-off&rdquo; means adjustment of losses against the profits from another source/head of income in the same assessment year. If losses cannot be set-off in the same year due to inadequacy of eligible profits, then such losses are carried forward to the next assessment year for adjustment against the eligible profits of that year. The maximum period for which different losses can be carried forward for set-off has been provided in the Act.<br>
<strong>Intra head set off/Inter source adjustment [Section 70]</strong><br>
Under this section, the losses incurred by the Assessee in respect of one source shall be set-off against income from any other source under the same head of income, since the income under each head is to be computed by grouping together the net result of the activities of all the sources covered by that head. In simpler terms, loss from one source of income can be adjusted against income from another source, both the sources being under the same head.<br>
<em>Example 1:</em>&nbsp;Loss from one house property can be set off against the income from another house property.<br>
<em>Example 2</em><strong>:&nbsp;</strong>Loss from one business, say textiles, can be set off against income from any other business, say printing, in the same year as both these sources of income fall under one head of income. Therefore, the loss in one business may be set-off against the profits from another business in the same year.<br>
<strong>Exemptions for the above:</strong><br>
<ol>
<li>Short-term capital loss is allowed to be set off against both short-term capital gain and long-term capital gain. However, long-term capital loss can be set-off only against long-term capital gain and not short-term capital gain.</li>
<li>A loss in speculation business can be set-off only against the profits of any other speculation business and not against any other business or professional income. However, losses from other business can be adjusted against profits from speculation business.</li>
<li>The losses incurred by an Assessee from the activity of owning and maintaining race horses cannot be set-off against the income from any other source other than the activity of owning and maintaining race horses.</li>
</ol>
<strong>Inter head adjustment [Section 71]</strong><br>
<strong>&nbsp;</strong>Loss under one head of income can be adjusted or set off against income under another head. However, the following points should be considered:<br>
<ol>
<li>Loss under head Capital Gains cannot be set-off with any other head of income.</li>
<li>Loss under the head Profits and gains from business or profession cannot be set off with income from salary</li>
<li>Speculation loss and loss from the activity of owning and maintaining race horses cannot be set off against income under any other head.</li>
</ol>
<strong>Loss from exempted source of income cannot be adjusted against taxable income:</strong><strong>&nbsp;</strong><br>
If income from a particular source is exempt from tax, then loss from such exempted source cannot be set off against any other income which is chargeable to tax.<br>
<strong>E.g.,</strong>&nbsp;Agricultural income is exempt from tax, if the taxpayer incurs loss from agricultural activity, then such loss cannot be adjusted against any other taxable income.<br>
