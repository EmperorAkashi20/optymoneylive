                              <p>An Income Tax benefit for lower income bracket is provided u/s 87A to an extent of INR 5000 [FY 2016-17] which has now reduced to INR 2500 [FY 2017-18] and onwards by the honorable FM.</p>
                              <p>Section 87A rebate provides for rebate from Income Tax to <b>RESIDENT INDIVIDUALS</b> earning income below specified limit.</p>
                              <strong>For FY 2017-18</strong>
                              The rebate u/s 87A can be claimed upon fulfillment of the following two conditions:
                              <p>1. The person claiming the rebate should be a RESIDENT INDIVIDUAL.</p>
                              <p>2. The person’s total Income Less Deductions (under Section 80) is equal to or less than Rs 3,50,000</p>
                              <p>If both the above conditions are satisfied, rebate of Rs 2,500 will be available under Section 87A. The rebate is limited to Rs 2,500. Which means if the total tax payable is lower than Rs 2,500, such lower amount of tax will be the rebate under section 87A. This rebate is applied on total tax before adding Education Cess(3%). This rebate is also available to Senior Citizens who are 60 years old but less than 80 years old. For FY 2016-17 The rebate u/s 87A can be claimed upon fulfillment of the following two conditions:</p>
                              <p>1. The person claiming the rebate should be a RESIDENT INDIVIDUAL.</p>
                              <p>2. The person’s total Income Less Deductions (under Section 80) is equal to or less than Rs 5,00,000.</p>
                              <p>If both the above conditions are satisfied, <strong>rebate of lower of Rs.5000 or Total tax payable</strong> will be available under Section 87A. This rebate is applied on total tax before adding Education Cess(3%). This rebate is also available to Senior Citizens who are 60 years old but less than 80 years <strong><em><u>Note:</u></em></strong><em> NRI and assessee other than Resident Individual can’t claim benefit under this section.</em></p>
