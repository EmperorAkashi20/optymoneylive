                              <p><strong><u>Yes</u></strong>, the method of computation of cost of acquisition for capital asset acquired before 1<sup>st</sup>&nbsp;April, 1981 differs. Generally, cost of acquisition of a capital asset is the cost incurred in acquiring the capital asset. It includes the purchase consideration plus any expenditure incurred exclusively for acquiring the capital asset.
                                 &nbsp;
                                 However, in respect of capital asset acquired before 1st April, 1981, the cost of acquisition will be&nbsp;<strong><u>higher</u></strong>&nbsp;of:
                              <ul>
                                 <li>the actual cost of acquisition of the asset; or<br>
                                 <li>fair market value of the asset as on 1st April, 1981.<br>
                              </ul>
                              This option is not available in the case of a depreciable asset.</p>
