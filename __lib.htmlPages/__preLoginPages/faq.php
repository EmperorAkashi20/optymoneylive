                              <button class="btn btn-link" type="button" data-toggle="collapse" data-target="#accordion-tab-6-content-10" aria-expanded="false" aria-controls="accordion-tab-6-content-10">In respect of capital asset acquired before 1st April, 1981 is there any special method to compute cost of acquisition?</button>
                          
