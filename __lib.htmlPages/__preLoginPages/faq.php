                        <div class="card-header" id="accordion-tab-6-heading-9">
                           
                              <button class="btn btn-link" type="button" data-toggle="collapse" data-target="#accordion-tab-6-content-9" aria-expanded="false" aria-controls="accordion-tab-6-content-9">Is the benefit of indexation available while computing capital gain arising on transfer of short-term capital asset?</button>
                         
                        </div>
                        <div class="collapse" id="accordion-tab-6-content-9" aria-labelledby="accordion-tab-6-heading-9" data-parent="#accordion-tab-6">
                           <div class="card-body">
                              <p><b>No,</b> the benefit of indexation is available only in case of long-term capital assets and is not available in case of short-term capital assets. Indexation is a process by which the cost of acquisition/improvement of a capital asset is adjusted against inflationary rise in the value of asset. Hence it is applicable only in the case of long term capital assets.</p>
