                     <h5 class="mb-0">
                     <button class="btn btn-link collapsed" data-toggle="collapse" data-target="#collapse5" aria-expanded="false" aria-controls="collapse5">
                        What is the difference between PAN and TAN?</button>
                     </h5>
                  </div>
                  <div id="collapse5" class="collapse" aria-labelledby="heading5" data-parent="#accordion">
                     <div class="card-body">
                        PAN stands for Permanent Account Number and TAN stands for Tax Deduction Account Number. TAN is to be obtained by the person responsible to deduct tax, i.e., the deductor. In all the documents relating to TDS and all the correspondence with the Income-tax Department relating to TDS one has to quote his TAN.<br>
                        PAN cannot be used for TAN, hence, the deductor has to obtain TAN, even if he holds PAN.<br>
                        However, in case of TDS on purchase of land and building (as per section 194-IA), the deductor is not required to obtain TAN and can use PAN for remitting the TDS.  
                     </div>
