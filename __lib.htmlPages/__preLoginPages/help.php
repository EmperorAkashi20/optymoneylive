                     <h5 class="mb-0">
                     <button class="btn btn-link collapsed" data-toggle="collapse" data-target="#collapse5" aria-expanded="false" aria-controls="collapse5">
                        What is tax deducted at source?</button>
                     </h5>
                  </div>
                  <div id="collapse5" class="collapse" aria-labelledby="heading5" data-parent="#accordion">
                     <div class="card-body">
                        For quick and efficient collection of taxes, the Income-tax Law has incorporated a system of deduction of tax at the point of generation of income. This system is called as “Tax Deducted at Source”, commonly known as TDS.<br>
                        Under this system tax is deducted at the origin of the income. Tax is deducted by the payer and is remitted to the Government by the payer on behalf of the payee.<br>
                        The provisions of deduction of tax at source are applicable to several payments such as salary, interest, commission, brokerage, professional fees, royalty, contract payments, etc.<br>
                        In respect of payments to which the TDS provisions apply, the payer has to deduct tax at source on the payments made by him and he has to deposit the tax deducted by him to the credit of the Government.  
                     </div>
