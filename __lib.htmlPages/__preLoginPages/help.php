                     <h5 class="mb-0">
                     <button class="btn btn-link collapsed" data-toggle="collapse" data-target="#collapse7" aria-expanded="false" aria-controls="collapse7">
                        Is it mandatory to file return of income after getting PAN?/ Who are required to file Return?</button>
                     </h5>
                  </div>
                  <div id="collapse7" class="collapse" aria-labelledby="heading7" data-parent="#accordion">
                     <div class="card-body">
                        It is not mandatory to file return of income after getting PAN. <br>
                        Return is to be filed only if you are liable to file return of income as under :<br>
                        <li>If your Gross total Income for financial Year 2017-18 (Income before allowing Sec80 Deductions) is more than Rs.2,50,000/-. (The limit is Rs.3, 00,000/- for the persons of age between 60-80years and Rs.5, 00,000/- for the persons above 80 years age).</li>
                        <li>If you wish to claim Refund of taxes deducted/ paid.</li>
                        <li>If you wish to carry forward losses to be set off in the next years return, then you have to declare losses for the current year by filing return.</li>
                        <li>If you are resident and having any asset outside India orhaving financial interest in any entity outside India then you have to file return of income even though you are not having any other incomes.</li>
                        <li>Return filing is compulsory even if you being a resident have signing authority in a foreign account.</li>
                        <li>In the above two points resident means only a resident and ordinary resident (ROR); it does not include resident but not ordinary resident (RNOR) or Non resident (NRI).</li>
                        <li>If you are having any exempt long term capital gains of more than Rs.2, 50,000/- in a financial year then you have to file the return compulsorily even though such gains are exempt from tax.(Here exempted long term capital gains include sale of equity shares, sale of units in equity oriented mutual funds, sale of units of a business trust)- effective form FY 2016-17.</li>
                        <li>If you are in receipt of any income derived from property held under a charitable or religious trust or a political party, educational institution, hospital, trade unions, any non profit organization, any authority, body or a trust etc., you have to file the income tax return compulsorily.</li>
                        <li>If you are planning to get any loan from bank or any other financial institution you may be asked copy of Income Tax Return.</li>
                        <li>In respective of a firm or a company, Return of Income needs to be filed irrespective of the income/loss of the firm or company during the financial year.</li>
