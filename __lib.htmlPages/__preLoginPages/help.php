                  <div id="collapse7" class="collapse" aria-labelledby="heading7" data-parent="#accordion">
                     <div class="card-body">
                        Although an intimation received by an assessee u/s 143(1) within a period of 1 Year from the end of Financial Year in which the Return of Income was filed for the year under assessment can be considered as Assessment by the authorized Assessing Officer, however it is a matter of judgment where 143(1) is taken as a mere Intimation rather than Assessment order.<br>
                        <u>Case Law: TATA AIG GENERAL INSURANCE PVT LTD Vs DCIT ITAT (Mumbai)- ITA No.968 (MUM)2015.</u><br>
                        <strong>Assessment under section 143(1)</strong><br>
                        This is intimation from the CPC and is a computer generated statement. All the data available in the Income Tax Return is validated by CPC with reference to the data available in the records of the income tax department.<br>
                        <strong>Scope of assessment under section 143(1)</strong><br>
                        Intimation under section 143(1) is like primary verification of the return of income filed. At this stage of verification detailed examination of the return of income is not carried out. At this stage, the total income or loss is computed after making the adjustments (if any). Some of the adjustments made can be for the following matters:-<br><br>
                        <li>The arithmetical errors in the return of income filed; or</li>
                        <li>Any incorrect claims made in the return of income;</li>
                        <li>Any loss claimed will be disallowed, if return of the previous year for which set-off of loss is claimed was furnished after the due date specified for filing the return of income;</li>
                        <li>Any expenditure mentioned in the audit report will be disallowed if it is not taken into consideration for the computation of total income in the return filed;</li>
                        <li>Any deduction claimed u/s 10AA, 80IA to 80-IE will also be disallowed, if the return is furnished after the due date specified for filing the return of income; or</li>
                        <li>Any additional income appearing in Form 26AS which was not included in computing the total income in the return of income.</li>
                        However, no adjustment shall be made by CPC unless intimation is given to the assessee of such adjustment either in writing or in electronic mode. So, the assessee will get the intimation about the adjustment. If the assessee gives any response, such response shall be considered before making any adjustment, and if no response is received within 30 days from the issue of the intimation, adjustments shall be made by the department.<br>
