               <h4 id="file26qc">File 26 QC (TDS on Rent of Property)</h4>
               <div class="card">
                  <div class="card-header" id="heading7">
                     <h5 class="mb-0">
                     <button class="btn btn-link collapsed" data-toggle="collapse" data-target="#collapse7" aria-expanded="false" aria-controls="collapse7">
                        What is TDS on Rent of Property and who is reasonable to deduct?</button>
                     </h5>
                  </div>
                  <div id="collapse7" class="collapse" aria-labelledby="heading7" data-parent="#accordion">
                     <div class="card-body">
                        As per Finance Act, 2017, “TDS on Rent” under section 194-IB is liable to be deducted by Individuals or HUFs (other than an individual who are not liable for Tax Audit u/s 44AB) who is paying to a resident monthly rent exceeding Rs.50,000. Section 194-IB of the Income Tax Act, 1961 states that for all the transactions with effect from June 1, 2017, tax @5% should be deducted by the Tenant/ Lessee/ Payer of the property at the time of making payment of rent (to Landlord / Lessor/ Payee). Tax so deducted should be deposited to the Government Account through any of the authorized bank branches.<br>
                        <strong><u>Applicability:</u></strong><br>
                        Individual or HUF who are not liable for tax audit under section 44AB.<br>
                        <strong><u>Limit &amp; Rate</u></strong><u>:</u><br>
                        Rent of Rs. 50,000 for a month or part of the month @ 5%.<br>
                        Note -&nbsp;<strong>This section does not applies to those rent which are covered under Section 194-I.</strong><br>
                        <strong><u>Highlights of Section 194-IB</u></strong><br>
                        <strong>No need to obtain TAN</strong>&nbsp;- Under this section, Tax payer need not to obtain TAN.<br>
                        <strong>Deduction, once in a year</strong>&nbsp;- Under this section, Tax payer is required to deduct tax at source only once in a financial.<br>
                        <strong>Time of deduction</strong>&nbsp;- The tax should be deducted at the time of credit or payment (whichever is earlier) of rent for the last month of the tax year or last month of tenancy if the property is vacated during the year, as applicable.<br>
                        <strong><em><u>Example</u></em></strong>&nbsp;– Mr A is a tenant of a property for which he is paying rent of Rs.60,000/- per Month and Mr A is not liable to tax audit u/s 44AB. Mr A is residing in the same property for whole Financial year 2018-19(April’18 to March’19).<br>
                        In the present case Mr A has to deduct TDS @ 5% while paying the rent for the month of March’19. The total amount of TDS to be deducted is Rs.7,20,000(60,000*12 = 7,20,000) X 5% is Rs.36,000/- .<br>
                        If the Rent/Lease agreement has been completed in between the Financial year, TDS should be deducted in last month of tenancy if the property is vacated during the year.<br>
